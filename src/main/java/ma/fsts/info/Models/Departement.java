package ma.fsts.info.Models;

public class Departement {

    private int idDep;
    private String nom;

    public Departement(int idDep, String nom){
        this.idDep = idDep;
        this.nom = nom;
    }

    public int getIdDep() {
        return idDep;
    }

    public String getNom() {
        return nom;
    }


    

}
